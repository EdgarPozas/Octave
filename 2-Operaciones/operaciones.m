
%variables

num1=10;
num2=3;

resultado=0;

disp(cstrcat('Los números son ',num2str(num1),' y ',num2str(num2)));

%suma
resultado=num1+num2;
disp(cstrcat('El resultado de la suma es : ',num2str(resultado)));

%resta
resultado=num1-num2;
disp(cstrcat('El resultado de la resta es : ',num2str(resultado)));

%multiplicación
resultado=num1*num2;
disp(cstrcat('El resultado de la multiplicación es : ',num2str(resultado)));

%divición
resultado=num1/num2;
disp(cstrcat('El resultado de la divición es : ',num2str(resultado)));

%potencia
resultado=num1**num2;
disp(cstrcat('El resultado de la potencia es : ',num2str(resultado)));

%raiz
resultado=sqrt(num1);
disp(cstrcat('El resultado de la raiz es : ',num2str(resultado)));



